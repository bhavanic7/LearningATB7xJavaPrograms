package July212024;

public class LAB20_doWhile1 {
    public static void main(String[] args) {
        // while, for -?
        // do while - while - 1 change - minor change
        // While -> int -> condition -> body -> increment / decrement
        // Do While
        // increment -> body -> condition -> increment / decrement
        int a = 0;
        do {
            System.out.println(a);
            a++;
        }while(a < 10); // if this condition fails, it will go to do loop again


    }
}
