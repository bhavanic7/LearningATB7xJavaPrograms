package July212024;

public class LAB25_RightTriangle {
    public static void main(String[] args) {
        /*Right Triangle Star Pattern
         *
         **
         ***
         ****
         *****   */
        int row = 6;
        for (int i = 0; i <= row; i++) {
            for (int j = 1; j <= i; j++) {

                System.out.println("*");

                System.out.println("\n");
            }

        }
    }

}